			<h4 class="green bigger">The Future</h4><p><span class="small">Our future programs will continue to enroll children 6 to 14 years of age and will provide instruction in instrumental music, acting, public speaking, and visual arts. The activities will be tailored to each community and coordinated with the local schools and youth bureaus, as well as with the local artist associations. For this purpose, we reached out to several elementary and middle schools in Romania and we built these relationships to successfully launch our programs after the first round of fundraising in 2014. At the same time, we contacted performing artists interested in collaborating with us and willing to participate as instructors for our programs. We see the role of local artists as a dual component of our mission: while engaging in our programs, the artists provide expert instruction and, at the same time, find a source of income in a profession generally known for its low monetary rewards. By supports local artists, we will furthermore enrich the exposure of the large community to cultural and artistic events.</span></p>
